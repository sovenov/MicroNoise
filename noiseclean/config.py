# ---------------------------------------------------------------------------
# Параметры обработки звука
# ---------------------------------------------------------------------------
# RNNoise работает строго на 48 кГц с кадром 480 сэмплов (10 мс).
# Speex-препроцессор тоже настраивается на эти значения, поэтому весь
# конвейер работает в едином формате 48 кГц / 480 сэмплов / моно.
SAMPLE_RATE = 48000
FRAME_SIZE = 480
FRAME_MS = FRAME_SIZE * 1000 // SAMPLE_RATE

# Режимы шумоподавления
MODE_LIGHT = "light"      # Speex
MODE_STRONG = "strong"    # RNNoise

# Уровень подавления Speex (дБ). Диапазон как в OBS: 0..-60.
SPEEX_NOISE_SUPPRESS_DB = -60

# RNNoise (интенсивный режим) — VAD-гейт поверх нейросети, как в
# werman/noise-suppression-for-voice. Единицы grace/retro — блоки по 10 мс.
RNNOISE_VAD_THRESHOLD = 0.6        # 0..0.99: если вероятность речи ниже — тишина
RNNOISE_VAD_GRACE_BLOCKS = 20      # держать открытым после речи (мин. 20 = 200 мс)
RNNOISE_RETRO_GRACE_BLOCKS = 0     # открывать назад к началу слов (задержка, макс. 99)
RNNOISE_MIN_GRACE_BLOCKS = 20
RNNOISE_MAX_RETRO_BLOCKS = 99

# Диапазон усиления выходного сигнала (после шумоподавления), дБ.
GAIN_MIN_DB = 0.0
GAIN_MAX_DB = 30.0

# Громкость локального мониторинга ("прослушивать свой микрофон").
MONITOR_VOLUME = 0.7

# Длительность тестовой записи, сек.
RECORD_SECONDS = 10


# ---------------------------------------------------------------------------
# Интерфейс (влияет ТОЛЬКО на отображение индикаторов, не на обработку звука)
# ---------------------------------------------------------------------------
# Период перерисовки индикаторов уровня в UI, мс. Больше число = реже обновление.
UI_TICK_MS = 33
# Сглаживание индикаторов входа/выхода, 0..1.
#   1.0  — без сглаживания (мгновенно, как было раньше);
#   0.2  — плавное скольжение полоски к новому значению;
#   меньше — ещё плавнее и медленнее.
UI_METER_SMOOTHING = 0.2


# ---------------------------------------------------------------------------
# Пути
# ---------------------------------------------------------------------------
def _base_dir():
    """Каталог ресурсов (libs/ и assets/).

    В сборке PyInstaller данные распаковываются в sys._MEIPASS (для onefile —
    во временную папку, для onedir — рядом с exe). Обычный запуск — каталог
    пакета noiseclean/.
    """
    if getattr(sys, "frozen", False):
        return getattr(sys, "_MEIPASS", os.path.dirname(sys.executable))
    return os.path.dirname(os.path.abspath(__file__))


BASE_DIR = _base_dir()
LIBS_DIR = os.path.join(BASE_DIR, "libs")
RNNOISE_DLL = os.path.join(LIBS_DIR, "rnnoise.dll")
SPEEX_DLL = os.path.join(LIBS_DIR, "libspeexdsp.dll")

ASSETS_DIR = os.path.join(BASE_DIR, "assets")
